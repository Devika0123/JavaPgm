package firstpgm;

public class Datatype {

	public static void main(java.lang.String[] args) {
		// Data types
		
		byte b=5;
		System.out.println("value of byte="+b);
		
		short s=6;
		System.out.println("value of short="+s);
		
		int v=10;
		System.out.println("value of int="+v);
		
		long l=98574262552l;
		System.out.println("value of long="+l);
		
		float f=10.58f;
		System.out.println("value of float="+f);
		
		double d=102.36;
		System.out.println("value of double="+d);
		
		char c='a';
		System.out.println("value of char="+c);
		
		char c1=69;
		System.out.println("value of c1="+c1);
		
		boolean t=10<20;
	    System.out.println("value of boolean="+t);
	    
	    java.lang.String w="hello";
	    System.out.println("value of string="+w);
	    
	    
	    
	   
	   }

}
